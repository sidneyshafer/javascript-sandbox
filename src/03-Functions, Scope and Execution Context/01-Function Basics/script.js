// Functions

// defining/declaring function
function sayHello() {
    console.log('Hello World!');
}

// calling function
sayHello();

// x and y are parameters
function add(x, y) {
    return x + y;
}

// 5 and 10 are arguments
const sum = add(5, 10);
console.log(sum);

function subtract(x, y) {
    return x - y;
}

console.log(subtract(10, 2));
