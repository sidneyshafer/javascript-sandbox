// WHAT IS AN OBJECT?
/**
 * 'Self-contained' piece of code that is a collection of methods and properties.
 * Objects are used a building blocks and can interact with one another.
 * There are many ways to create objects in JS.
 * We have object literals, constructor functions, classes, factories and more.
 * We have worked with many objects already.
 * We have even created some using the object literal syntax.
 */

