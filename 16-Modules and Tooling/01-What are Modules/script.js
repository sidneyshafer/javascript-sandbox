// WHAT ARE MODULES?
/**
 * Modules are JavaScript files we can import into other JavaScript files.
 * Can export and import things like variables, functions and classes.
 * Can also import 3rd-party packages from NPM (Node Package Manager)
 */

// WHY USE MODULES & TOOLING?
/**
 * Makes code much more organized and easy to read/write by breaking it up into seperate files.
 * Reusibility, NPM packages, Optimization, Customized environment
 */

// TYPES OF MODULES
/**
 * Common JS Modules - commonly used by Node.js
 * ES Modules (ESM, ES6 Modules) - commonly used in the front-end
 * 
 * modules are not supported in older browsers. There is support for ES Modules in newer browsers, but it is still recommended to use a module bundler.
 */

// WHAT IS NODE.JS?
/**
 * Node is a JavaScript runtime environment. 
 * It allows JavaScript to run on the server, much like any other language such as Python, Ruby, Java, ect.
 * Node uses the V8 JS engine, which is the same engine that Chrome uses.
 * The inner workings are similar to the browser, since it uses the V8 engine.
 * There is no 'Window' or 'Document' in Node
 */